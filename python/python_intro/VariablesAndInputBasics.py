# このファイルは、Python における変数の宣言、基本演算、キーボード入力の使い方を学ぶ練習用コードです。
# 文字列と数値の操作、演算子の使い方、ユーザー入力の処理までを順を追って確認できます。

# --- 変数への代入と出力 ---
# 数値の変数を宣言して出力
sale = 10
print("売り上げは", sale, "万円です。")

# --- 変数の値を変更する ---
# 変数の値を変更して出力
sale = 10
print("売り上げは", sale, "万円です。")
print("売上の値を変更します。")
sale = 20
print("売り上げは", sale, "万円です。")

# --- 文字列と変数の組み合わせ ---
# 文字列と数値の変数を組み合わせて出力
name = "東京"
sale = 10
print(name, "支店の売上は", sale, "万円です。")

# --- 式の直接出力と変数への計算結果の格納 ---
# 式を直接print文に書く
print("1+2は", 1 + 2, "です。")

# 計算結果を変数に入れて出力
price = 50
num = 10
total = price * num
print("単価は", price, "円です。")
print("売上個数は", num, "個です。")
print("合計金額は", total, "円です。")

# 値引き後の計算と出力
total = total - 100
print("値引きすると", total, "円です。")

# --- 演算子の使い方 ---
# 様々な演算子の利用例
num1 = 10
num2 = 5

print("num1+num2は", num1 + num2, "です。")
print("num1-num2は", num1 - num2, "です。")
print("num1*num2は", num1 * num2, "です。")
print("num1/num2は", num1 / num2, "です。")
print("num1%num2は", num1 % num2, "です。")

# --- 繰り返し文字列による簡易グラフ表示 ---
# 文字列を繰り返して簡単なグラフを表示
num = 10
pic = "〇"
graph = pic * num
print("売上：" + graph)
print(num, "万円の売上があります。")

# --- キーボード入力と出力 ---
# キーボードからの入力を受け取り、そのまま出力
n = input("値を入力してください。")
print(n, "が入力されました。")

# 整数を2つ入力して合計を計算・出力
num1 = int(input("整数1を入力してください。"))
num2 = int(input("整数2を入力してください。"))
num3 = num1 + num2
print(num1, "+", num2, "は", num3, "です。")

# 年齢をキーボードから入力し、出力する
num = int(input("あなたは何才ですか？"))
print("あなたは", num, "才です。")

# 身長と体重をキーボードから入力し、出力する
num1 = float(input("身長を入力してください。"))
num2 = float(input("体重を入力してください。"))
print("身長は", num1, "センチです。")
print("体重は", num2, "キロです。")
