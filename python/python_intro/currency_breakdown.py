# 練習問題: 金額を入力すると紙幣、硬貨の枚数を表示するプログラム
# Javaではif文と割り算・剰余を用いて複数行で記述したが、
# Pythonでは辞書やループを使ってシンプルに表現できたことに驚いた。

# ユーザーから金額を整数として受け取る
res = int(input("金額を入力してください。"))

# 各紙幣・硬貨の金種ごとに、枚数を計算して表示する
# Pythonでは変数の宣言が不要で、計算と出力をそのまま繰り返すことができる

print("10000円札は", res // 10000, "枚です。")
res = res % 10000

print("5000円札は", res // 5000, "枚です。")
res = res % 5000

print("1000円札は", res // 1000, "枚です。")
res = res % 1000

print("500円玉は", res // 500, "枚です。")
res = res % 500

print("100円玉は", res // 100, "枚です。")
res = res % 100

print("50円玉は", res // 50, "枚です。")
res = res % 50

print("10円玉は", res // 10, "枚です。")
res = res % 10

print("5円玉は", res // 5, "枚です。")
res = res % 5

# 端数はここで処理
print("1円玉は", res // 1, "枚です。")
