# Python の基本的な出力とリテラルの学習記録
# 出力方法（print）、数値リテラル、エスケープシーケンス、raw文字列、タブ文字の扱いなど

# 文字列の基本出力
print("ようこそPythonへ!")
print("Pythonをはじめましょう!")

# エスケープシーケンスの誤用例（途中で改行すると構文エラーになる）
print(
    "Pythonをはじめ\
#ましょう"
)

# 数値リテラルの出力（10進数・2進数・8進数・16進数）
print("10進数の10は", 10, "です。")
print("2進数の10は", 0b10, "です。")
print("8進数の10は", 0o10, "です。")
print("16進数の10は", 0x10, "です。")
print("16進数のFは", 0xF, "です。")

# エスケープ文字の例（円記号やアポストロフィ）
print("円記号を表示します。:¥¥")  # ¥マークを2つ書くと1つ表示される
print("アポストロフィを表示します。:¥'")  # ¥' でアポストロフィ（'）を表示

# raw文字列の使用（エスケープせずにそのまま表示）
print(r"円記号を表示します。:¥¥")

# 数値やバックスラッシュを含む文字列の出力
print(123)
print(r"\ 100 もらった")  # バックスラッシュをそのまま表示
print("またあした")

# タブ文字（\t）による整列表示
print("1\t2\t3\t4\t5\t6")

