# Pythonの基本的な出力（print）を試す
print("ようこそPythonへ!")
print("Pythonをはじめましょう!")

# エスケープシーケンスの誤用例（# 途中で改行してしまっている）
print("Pythonをはじめ\
#ましょう")

# 数値リテラルの表示（10進数、2進数、8進数、16進数）
print("10進数の10は", 10, "です。")
print("2進数の10は", 0b10, "です。")
print("8進数の10は", 0o10, "です。")
print("16進数の10は", 0x10, "です。")
print("16進数のFは", 0xF, "です。")

# エスケープ文字を使った文字の表示
print("円記号を表示します。:¥¥")  # ¥マークを2つ書くと1つ表示される
print("アポストロフィを表示します。:¥'")  # ¥' でアポストロフィ（'）を表示

# raw文字列を使ってエスケープせずにそのまま表示
print(r"円記号を表示します。:¥¥")

# 数値と文字列の基本的な表示
print(123)
print(r"\ 100 もらった")  # バックスラッシュをそのまま表示
print("またあした")

# タブ文字を使った出力
print("1\t2\t3\t4\t5\t6")